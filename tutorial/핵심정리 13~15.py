# 조건식과 == 연산자
"""
조건식을 작성할 때 == 연산자 대신 = 연산자를 사용하는 실수를 하지 않도록 주의합니다.
if x == 1:    # 올바른 조건식
    print(x)

if x = 1:     # 잘못된 조건식
    print(x)
"""

