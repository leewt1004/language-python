## 25.2 반복문으로 딕셔너리의 키-값 쌍을 모두 출력하기

"""------------------------------------------------------------------------------------------------------------------------------------------------------"""
""" 딕셔너리와 for 반복문을 사용하여 간단하게 모든 키-값 쌍을 출력해보겠습니다. """
x = {'a': 10, 'b': 20, 'c': 30, 'd': 40}
for i in x :
    print(i, end=' ')                   # a b c d
"""------------------------------------------------------------------------------------------------------------------------------------------------------"""

"""------------------------------------------------------------------------------------------------------------------------------------------------------"""
"""
키와 값을 모두 출력 

for 키, 값 in 딕셔너리.items():
    반복할 코드
"""
x = {'a': 10, 'b': 20, 'c': 30, 'd': 40}
for key, value in x.items() :
    print(key, value)
"""------------------------------------------------------------------------------------------------------------------------------------------------------"""

"""------------------------------------------------------------------------------------------------------------------------------------------------------"""
""" in 다음에 딕셔너리를 직접 지정하고 items를 사용해도 상관 없습니다. """
for key, value in {'a': 10, 'b': 20, 'c': 30, 'd': 40}.items():
    print(key, value)
"""------------------------------------------------------------------------------------------------------------------------------------------------------"""


### 25.2.1  딕셔너리의 키만 출력하기
""" 
지금까지 items로 키와 값을 함께 가져왔는데, 키만 가져오거나 값만 가져오면서 반복할 수도 있습니다.

items: 키-값 쌍을 모두 가져옴
keys: 키를 모두 가져옴
values: 값을 모두 가져옴
먼저 for 반복문에서 keys로 키를 가져오면서 반복해보겠습니다.
"""
x = {'a': 10, 'b': 20, 'c': 30, 'd': 40}
for key in x.keys() :
    print(key, end=' ')                 # a b c d


### 25.2.2  딕셔너리의 값만 출력하기
""" for 반복문에서 values를 사용하면 값만 가져오면서 반복할 수 있습니다. """
x = {'a': 10, 'b': 20, 'c': 30, 'd': 40}
for value in x.values() :
    print(value, end=' ')                   # 10 20 30 40
